/**
	\fn  unsigned char Teclado( void )
	\brief Lectura del buffer de teclado Matricial de la expansion 3
 	\param void
	\return codigo de tecla si hay tecla en el buffer o NO_KEY si no la hay
*/
unsigned char Get_Teclado_Matricial( void )
{
	unsigned char Key = NO_KEY;

	//TimerEvent( );

	if (key_Expansion3 != NO_KEY )
	{
		Key = key_Expansion3;
		key_Expansion3 = NO_KEY;
	}
	return Key;
}
