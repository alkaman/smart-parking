/**
	\fn  void RGB_Parpadear(uint8_t selector)
	\brief Hace parpadear el RGB
 	\details
 	\param 	[in] uint8_t selector
			0,1,2 dependiendo del color a parpadear
			R,G,B
 	\return void
 	\author Alan Kinast, Marcos Trautman
 	\date 2016.11.25
*/

void RGB_Parpadear(uint8_t selector)
{
	RGB(selector,2);
}

/**
	\fn  void RGB_Apagar(void)
	\brief Apaga los 3 led del RGB
 	\details
 	\param 	[in] void
 	\return void
 	\author Alan Kinast, Marcos Trautman
 	\date 2016.11.25
*/

void RGB_Apagar(void)
{
	RGB(1,0);
	RGB(2,0);
	RGB(3,0);
}
